public class TaskTaskPair {
    private String task1;
    private String task2;

    public TaskTaskPair (String task1, String task2) {
        this.task1 = task1;
        this.task2 = task2;
    }

    String getTask1() {
        return this.task1;
    }

    String getTask2() {
        return this.task2;
    }
}
